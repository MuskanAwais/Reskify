// Function to get all tasks for a specific trade
export function getAllTasksForTrade(tradeName: string): string[] {
  const trade = MASSIVE_TASKS_DATABASE[tradeName as keyof typeof MASSIVE_TASKS_DATABASE];
  if (!trade) return [];
  
  const allTasks: string[] = [];
  Object.values(trade).forEach(categoryTasks => {
    allTasks.push(...categoryTasks);
  });
  
  return allTasks;
}

// Function to get all categories for a trade
export function getCategoriesForTrade(tradeName: string) {
  const trade = MASSIVE_TASKS_DATABASE[tradeName as keyof typeof MASSIVE_TASKS_DATABASE];
  if (!trade) return [];
  
  return Object.entries(trade).map(([categoryName, tasks]) => ({
    name: categoryName,
    tasks: tasks,
    isPrimary: categoryName.includes('Primary') || categoryName.includes('Structural'),
    totalTasks: tasks.length
  }));
}

// Function to search tasks across all trades
export function searchAllTasks(searchTerm: string, tradeFilter?: string): any[] {
  const results: any[] = [];
  const searchLower = searchTerm.toLowerCase();
  
  const tradesToSearch = tradeFilter 
    ? [tradeFilter] 
    : Object.keys(MASSIVE_TASKS_DATABASE);
    
  tradesToSearch.forEach(tradeName => {
    const trade = MASSIVE_TASKS_DATABASE[tradeName as keyof typeof MASSIVE_TASKS_DATABASE];
    if (!trade) return;
    
    Object.entries(trade).forEach(([categoryName, tasks]) => {
      tasks.forEach(task => {
        if (task.toLowerCase().includes(searchLower)) {
          results.push({
            task,
            trade: tradeName,
            category: categoryName
          });
        }
      });
    });
  });
  
  return results;
}

// Function to get total task count
export function getTotalTaskCount(): number {
  let total = 0;
  Object.values(MASSIVE_TASKS_DATABASE).forEach(trade => {
    Object.values(trade).forEach(categoryTasks => {
      total += categoryTasks.length;
    });
  });
  return total;
}

// Function to get task count by trade
export function getTaskCountByTrade(): Record<string, number> {
  const counts: Record<string, number> = {};
  
  Object.entries(MASSIVE_TASKS_DATABASE).forEach(([tradeName, trade]) => {
    let tradeTotal = 0;
    Object.values(trade).forEach(categoryTasks => {
      tradeTotal += categoryTasks.length;
    });
    counts[tradeName] = tradeTotal;
  });
  
  return counts;
}