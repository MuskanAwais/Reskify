// Export function to get all trades
export function getAllUltimateConstructionTrades() {
  return Object.keys(ULTIMATE_CONSTRUCTION_DATABASE);
}

// Export function to get trade data
export function getUltimateTradeData(tradeName: string) {
  return ULTIMATE_CONSTRUCTION_DATABASE[tradeName as keyof typeof ULTIMATE_CONSTRUCTION_DATABASE];
}

// Export function to get all activities for a trade
export function getUltimateTradeActivities(tradeName: string) {
  const tradeData = getUltimateTradeData(tradeName);
  if (!tradeData) return [];
  
  return Object.values(tradeData).flat();
}

// Export function to count activities per trade
export function countUltimateTradeActivities() {
  const trades = getAllUltimateConstructionTrades();
  return trades.reduce((acc, trade) => {
    const activities = getUltimateTradeActivities(trade);
    acc[trade] = activities.length;
    return acc;
  }, {} as Record<string, number>);
}