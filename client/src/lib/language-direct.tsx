const currentLanguage = (() => {
  if (typeof window !== "undefined") {
    return localStorage.getItem("selectedLanguage") || "en";
  }
  return "en";
})();

export function translate(key: string): string {
  const translations_typed = translations as Record<string, Record<string, string>>;
  const currentTranslations = translations_typed[currentLanguage];
  
  if (!currentTranslations) {
    console.warn(`No translations found for language: ${currentLanguage}, falling back to English`);
    return translations_typed["en"][key] || key;
  }
  
  const translation = currentTranslations[key];
  
  if (!translation) {
    console.warn(`Translation missing for key: ${key} in language: ${currentLanguage}, falling back to English`);
    return translations_typed["en"][key] || key;
  }
  
  return translation;
}

export function getCurrentLanguage(): string {
  return currentLanguage;
}

export function setLanguage(language: string) {
  if (typeof window !== "undefined") {
    localStorage.setItem("selectedLanguage", language);
    window.location.reload();
  }
}

